  DEFAULT_WAREHOUSE = IOT_INGEST_WH
  DEFAULT_NAMESPACE = 'IOT_PLATFORM.BRONZE'
  COMMENT           = 'IoT Hackathon - Snowflake Kafka Connector service user (key-pair auth only)'
  MUST_CHANGE_PASSWORD = FALSE;

