-- ----------------------------------------------------------------------------
-- 7. Grants — minimum required for the Snowflake Kafka Connector to operate.
--    The connector uses Snowpipe internally, so it needs to create/manage its
--    own internal stage and pipe objects per topic-partition, in addition to
--    writing rows into the target table.
-- ----------------------------------------------------------------------------
GRANT USAGE ON WAREHOUSE IOT_INGEST_WH TO ROLE KAFKA_CONNECTOR_ROLE;

GRANT USAGE ON DATABASE IOT_PLATFORM TO ROLE KAFKA_CONNECTOR_ROLE;

GRANT USAGE ON SCHEMA IOT_PLATFORM.BRONZE TO ROLE KAFKA_CONNECTOR_ROLE;
GRANT CREATE TABLE ON SCHEMA IOT_PLATFORM.BRONZE TO ROLE KAFKA_CONNECTOR_ROLE;
GRANT CREATE STAGE ON SCHEMA IOT_PLATFORM.BRONZE TO ROLE KAFKA_CONNECTOR_ROLE;
GRANT CREATE PIPE ON SCHEMA IOT_PLATFORM.BRONZE TO ROLE KAFKA_CONNECTOR_ROLE;

-- ----------------------------------------------------------------------------
-- 8. Bronze table — IOT_EVENTS_RAW, using the standard Snowflake Kafka
--    Connector table shape: two VARIANT columns. This is deliberate for a
--    Bronze layer - it preserves the FULL Debezium CDC envelope
--    (before/after/source/op/ts_ms) exactly as received, unparsed. Typed,
--    per-field columns belong in the Silver layer (a future dbt task), not
--    here - Bronze is schema-on-read by design.
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS IOT_PLATFORM.BRONZE.IOT_EVENTS_RAW (
  RECORD_METADATA VARIANT COMMENT 'Kafka metadata: topic, partition, offset, timestamp, key',
  RECORD_CONTENT  VARIANT COMMENT 'Full Debezium CDC envelope: before, after, source, op, ts_ms'
)
COMMENT = 'Bronze layer - raw Debezium CDC events from cdc.public.iot_events, ingested by the Snowflake Kafka Connector';

-- The connector's role needs to write into (and own the lifecycle of) this
-- table, since the Kafka Connector may alter it as part of normal operation.
GRANT OWNERSHIP ON TABLE IOT_PLATFORM.BRONZE.IOT_EVENTS_RAW TO ROLE KAFKA_CONNECTOR_ROLE
  REVOKE CURRENT GRANTS;

