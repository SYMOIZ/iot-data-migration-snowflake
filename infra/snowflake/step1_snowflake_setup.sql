SHOW SCHEMAS LIKE 'BRONZE' IN DATABASE IOT_PLATFORM;
SHOW ROLES LIKE 'KAFKA_CONNECTOR_ROLE';
SHOW USERS LIKE 'KAFKA_CONNECTOR_USER';
DESC USER KAFKA_CONNECTOR_USER;  -- confirm RSA_PUBLIC_KEY_FP is set (not null)
SHOW TABLES LIKE 'IOT_EVENTS_RAW' IN SCHEMA IOT_PLATFORM.BRONZE;
