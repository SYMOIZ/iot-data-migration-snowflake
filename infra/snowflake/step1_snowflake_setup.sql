USE ROLE ACCOUNTADMIN;

-- ----------------------------------------------------------------------------
-- 1. Warehouse — compute for the Kafka Connector's ingestion (Snowpipe-backed)
-- ----------------------------------------------------------------------------
CREATE WAREHOUSE IF NOT EXISTS IOT_INGEST_WH
  WAREHOUSE_SIZE      = 'XSMALL'
  AUTO_SUSPEND         = 60
  AUTO_RESUME          = TRUE
  INITIALLY_SUSPENDED  = TRUE
  COMMENT              = 'IoT Hackathon - Kafka Connector ingestion warehouse';

-- ----------------------------------------------------------------------------
-- 2. Database
-- ----------------------------------------------------------------------------
CREATE DATABASE IF NOT EXISTS IOT_PLATFORM
  COMMENT = 'IoT Hackathon - Bronze/Silver/Gold data platform';

-- ----------------------------------------------------------------------------
-- 3. Schema — Bronze layer (raw, untransformed CDC events)
-- ----------------------------------------------------------------------------
CREATE SCHEMA IF NOT EXISTS IOT_PLATFORM.BRONZE
  COMMENT = 'Bronze layer - raw Debezium CDC events, schema-on-read (VARIANT)';

-- ----------------------------------------------------------------------------
-- 4. Role — least-privilege role for the Kafka Connector only
-- ----------------------------------------------------------------------------
CREATE ROLE IF NOT EXISTS KAFKA_CONNECTOR_ROLE
  COMMENT = 'IoT Hackathon - role for the Snowflake Kafka Connector service user';

-- ----------------------------------------------------------------------------
-- 5. User — service account for the Kafka Connector, key-pair auth only
--    (no password - RSA_PUBLIC_KEY set below; this account cannot log in
--    via username/password at all, matching the no-static-password pattern
--    used everywhere else in this project)
-- ----------------------------------------------------------------------------
